/**
 * Class to create Primitive objects such as int, float and boolean
 * @author sid
 *
 */
public class PrimitiveObj {
	private int intVal;
	private float floatVal;
	private boolean boolVal;
	
	public PrimitiveObj() {
	}
	
	public PrimitiveObj(int val1, float val2, boolean val3) {
		intVal = val1;
		floatVal = val2;
		boolVal = val3;
	}
}
